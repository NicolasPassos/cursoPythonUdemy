## Trabalhando com operadores de atribuição
"""
= += -= *= /= //= **= %=
"""
contador = 10

contador += 5 ## Mesma coisa que contator = contador + 5
contador -= 5 ## Mesma coisa que contator = contador - 5
contador /= 5 ## Mesma coisa que contator = contador / 5
contador //= 5 ## Mesma coisa que contator = contador // 5
contador **= 5 ## Mesma coisa que contator = contador ** 5
contador %= 5 ## Mesma coisa que contator = contador % 5