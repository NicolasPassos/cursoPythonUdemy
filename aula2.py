"""
DocString
Python é uma Linguagem de programação
Tipo de tipagem = Dinâmica (já sabe o tipo de informação que é passado) / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Nicolas Duarte')
print(1, 'Nicolas Duarte"')

# Aspas duplas
print("Nicolas")
print(2, "Nicolas Duarte'")

# Escape (para colocar aspas dentro do texto)
# Alternativa mais fácil é fazer o uso de aspas simples dentro de uma string declarada com aspas duplas e vice e versa
print("Nicolas \"Duarte\"") 

# r (para exibir o caractere de escape, usado para expressões regulares)
print(r"Nicolas \"Duarte\"")